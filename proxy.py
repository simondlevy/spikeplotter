import socket
from struct import pack

sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
sock.bind(('localhost', 5000))
sock.listen(1)

conn, _ = sock.accept()

while True:

    try:

        conn.send(pack('III', 30, 40, 200))

    except Exception:

        break
